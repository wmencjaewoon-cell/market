-- Estimate requests and store-side customer/quote management.

create or replace function public.is_verified_store_user()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and user_type = 'store'
      and coalesce(business_verified, false)
      and coalesce(store_accepts_inquiries, true)
      and coalesce(status, 'active') = 'active'
  );
$$;

grant execute on function public.is_verified_store_user() to authenticated;

create table if not exists public.estimate_requests (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete set null,
  category text not null,
  region text,
  address text,
  budget text,
  desired_date date,
  title text not null,
  description text,
  status text not null default 'open',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.estimate_requests
  add column if not exists preferred_contact text,
  add column if not exists admin_note text;

alter table public.estimate_requests enable row level security;

alter table public.estimate_requests
  drop constraint if exists estimate_requests_status_check;

alter table public.estimate_requests
  add constraint estimate_requests_status_check
  check (status in ('open', 'contacted', 'closed', 'hidden'));

create index if not exists estimate_requests_created_idx
on public.estimate_requests (created_at desc);

create index if not exists estimate_requests_category_region_idx
on public.estimate_requests (category, region);

drop policy if exists estimate_requests_select_own_or_admin on public.estimate_requests;
drop policy if exists estimate_requests_select_participants on public.estimate_requests;
create policy estimate_requests_select_participants
on public.estimate_requests
for select
using (
  auth.uid() = user_id
  or public.is_admin()
  or public.is_verified_store_user()
);

drop policy if exists estimate_requests_insert_own on public.estimate_requests;
create policy estimate_requests_insert_own
on public.estimate_requests
for insert
with check (auth.uid() = user_id);

drop policy if exists estimate_requests_update_own_or_admin on public.estimate_requests;
create policy estimate_requests_update_own_or_admin
on public.estimate_requests
for update
using (auth.uid() = user_id or public.is_admin())
with check (auth.uid() = user_id or public.is_admin());


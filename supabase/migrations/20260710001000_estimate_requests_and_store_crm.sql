on public.estimate_quote_attachments
for select
using (
  exists (
    select 1
    from public.estimate_quotes q
    join public.estimate_requests er on er.id = q.estimate_request_id
    where q.id = quote_id
      and (
        q.store_user_id = auth.uid()
        or er.user_id = auth.uid()
        or public.is_admin()
      )
  )
);

